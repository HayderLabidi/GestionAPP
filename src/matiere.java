    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
          // Modify selected row in the table
        String matiere = jTextField1.getText();
    String newCoef = jTextField2.getText();
    String newOption = jTextField3.getText();
    String newDepartement = jComboBox1.getSelectedItem().toString();

    try {
        String sql = "UPDATE matiere SET coef=?, options=?, departement=? WHERE matiere=?";
        prepared = cnx.prepareStatement(sql);
        prepared.setString(1, newCoef);
        prepared.setString(2, newOption);
        prepared.setString(3, newDepartement);
        prepared.setString(4, matiere);
        int rowsAffected = prepared.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Matière modifiée avec succès.");
        } else {
            JOptionPane.showMessageDialog(null, "Matière introuvable.");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Erreur lors de la modification de la matière: " + ex.getMessage());
        ex.printStackTrace();
    }
        
    }//GEN-LAST:event_jButton2ActionPerformed

