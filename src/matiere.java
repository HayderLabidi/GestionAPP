    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         // Refresh the table (in this example, simply reloads the data model)
        try {
        String sql = "SELECT matiere, coef, options, departement FROM matiere";
        prepared = cnx.prepareStatement(sql);
        ResultSet resultSet = prepared.executeQuery();
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Matiere");
        model.addColumn("Coef");
        model.addColumn("Options");
        model.addColumn("Département");
        
        while (resultSet.next()) {
            String nom = resultSet.getString("matiere");
            String coef = resultSet.getString("coef");
            String option = resultSet.getString("options");
            String departement = resultSet.getString("departement");
            model.addRow(new Object[]{nom, coef, option, departement});
        }
        
        jTable2.setModel(model);
        
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Erreur lors de l'affichage des matières: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_jButton4ActionPerformed

